package Lab_27042024;
/*for loop, while loop , do loop are used
  when there are repetation of checks required on the condition based on condition.

   syntax for (A:B:C)
  A -? initialization - where you want to start
  B - Condition(till where you want to go) - i<10
  c - How you want to increment/decrement/update
   */
public class For_Loop {
    public static void main(String[] args) {


        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }
}
