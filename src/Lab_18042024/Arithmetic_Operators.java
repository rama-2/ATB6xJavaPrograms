package Lab_18042024;

public class Arithmetic_Operators {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        String name = "Ramadevi";
        System.out.println(x+name);
        System.out.println(name+x+y);
    }
}
